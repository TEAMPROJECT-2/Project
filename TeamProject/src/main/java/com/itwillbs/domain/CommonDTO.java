package com.itwillbs.domain;

public class CommonDTO {

	private String cdOpt;
	private String cdOptNm;
	private String highCd;
	private String useYn;
	private String srhHighCd;
	private String[] srhCdOpt;

	public String getCdOpt() {
		return cdOpt;
	}
	public void setCdOpt(String cdOpt) {
		this.cdOpt = cdOpt;
	}
	public String getCdOptNm() {
		return cdOptNm;
	}
	public void setCdOptNm(String cdOptNm) {
		this.cdOptNm = cdOptNm;
	}
	public String getHighCd() {
		return highCd;
	}
	public void setHighCd(String highCd) {
		this.highCd = highCd;
	}
	public String getUseYn() {
		return useYn;
	}
	public void setUseYn(String useYn) {
		this.useYn = useYn;
	}
	public String getSrhHighCd() {
		return srhHighCd;
	}
	public void setSrhHighCd(String srhHighCd) {
		this.srhHighCd = srhHighCd;
	}
	public String[] getSrhCdOpt() {
		return srhCdOpt;
	}
	public void setSrhCdOpt(String[] srhCdOpt) {
		this.srhCdOpt = srhCdOpt;
	}


}